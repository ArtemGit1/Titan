const modal = document.getElementById('trainerModal');
const modalBody = document.getElementById('trainerModalBody');
const closeBtn = document.getElementById('closeTrainerModal');

document.querySelectorAll('.trainer-details-btn').forEach(btn => {
  btn.addEventListener('click', function() {
    const key = this.getAttribute('data-trainer');
    const t = trainersData[key];
    if (t) {
      modalBody.innerHTML = `
        <img src="${t.photo}" alt="${t.name}" class="trainer-modal-photo">
        <h3 class="trainer-modal-name">${t.name}</h3>
        <div class="trainer-modal-desc">${t.description}</div>
        <div class="trainer-modal-achievements"><b>–î–æ—Å—è–≥–Ω–µ–Ω–Ω—è:</b> ${t.achievements}</div>

      `;
      modal.style.display = 'flex';
      document.body.style.overflow = 'hidden';
    }
  });
});

closeBtn.addEventListener('click', function() {
  modal.style.display = 'none';
  document.body.style.overflow = '';
});

window.addEventListener('click', function(e) {
  if (e.target === modal) {
    modal.style.display = 'none';
    document.body.style.overflow = '';
  }
});
